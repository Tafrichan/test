  train <- pres.points2[kfold_pres!= i,]#for presence values, select all folds which are not 'i' to train the model
  test <- pres.points2[kfold_pres == i,]#the remaining fold is used to test the model
  backTrain<-abs.points2[kfold_back!=i,]#now for background values, select training folds
  backTest<-abs.points2[kfold_back==i,]#use the remainder for model testing
  dataTrain<-rbind(train,backTrain)#bind presence and background training data together
  dataTest<-rbind(test,backTest)#bind test data together
  bc_gajah_K<-bioclim(train[,1:8])#for bioclim we only need presence data to train the model!
  bc_K[[i]] <- evaluate(p=test[,1:8],a=backTest[,1:8], bc_gajah_K)#use testing data (kfold==i) for model evaluation
  #check the AUC by plotting ROC values
  plot(bc_K[[i]],'ROC')
}
